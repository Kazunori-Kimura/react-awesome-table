export interface User {
    id: string;
    name: string;
    kana: string;
    tel: string;
    email: string;
    zip: string;
    address: string;
}
function isNotNullish(item: unknown): item is Record<string, unknown> {
    if (item && typeof item === 'object') {
        return true;
    }
    return false;
}

export function isUser(item: unknown): item is User {
    if (isNotNullish(item)) {
        return (
            typeof item.id === 'string' &&
            typeof item.name === 'string' &&
            typeof item.kana === 'string' &&
            typeof item.tel === 'string' &&
            typeof item.email === 'string' &&
            typeof item.zip === 'string' &&
            typeof item.address === 'string'
        );
    }
    return false;
}

